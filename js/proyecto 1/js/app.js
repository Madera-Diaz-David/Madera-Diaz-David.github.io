
//Defino las variables 
var a,b, suma; 

//Capturar los datos del usuario
a=parseInt(prompt("ingrese el primer numero: "));
b=parseInt(prompt("ingrese el segundo numero: "));

//Proceso de la suma
suma=a+b; 
//Mensaje que se muestra en la consola
console.log("La suma de " +a+ " + " +b+ " es: " + suma);

//Mensaje de salida
document.write ("La suma de " +a+ " + " +b+ " es: " + suma);
//Mensaje de alerta
alert("La suma de " +a+ " + " +b+ " es: " + suma);